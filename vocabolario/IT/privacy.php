<p>
	<h3>Informativa sul trattamento dei dati personali</h3>
</p>
<p>
	Vi informiamo che a seguito della consultazione e l'utilizzo di questa
	piattaforma telematica possono essere trattati dati relativi a persone
	identificate o identificabili. Il trattamento dei dati personali
	avverr&#224; presso la sede del Titolare del trattamento e del Responsabile
	del trattamento mediante strumenti automatizzati e per il tempo
	strettamente necessario a conseguire gli scopi per cui sono stati raccolti.
	<br/>
	Specifiche misure di sicurezza sono osservate per prevenire la perdita dei
	dati, usi illeciti o non corretti ed accessi non autorizzati.
	<br/>
	In linea generale, &#232; opportuno precisare che, attraverso la presente
	piattaforma telematica, vengono effettuate diverse tipologie di raccolta
	dati:
</p>
<ol start="1" type="1">
	<li>
		raccolta necessaria ed automatica dei dati del navigatore inerenti
		all'interazione con il sito web;
	</li>
	<li>
		trattamento correlato alla registrazione dell'operatore economico alla
		piattaforma;
	</li>
	<li>
		trattamento correlato all'acquisizione delle informazioni da parte
		della Stazione Appaltante al fine di gestire il procedimento
		(acquisizione e gestione delle informazioni richieste per partecipare
		alla procedura);
    </li>
    <li>
        trattamento correlato alla verifica dei requisiti di ordine generale e
	    speciale (lex specialis di gara) in capo all'operatore;
    </li>
    <li>
        trattamento correlato alle comunicazioni agli indirizzi indicati per
        usufruire del servizio di assistenza tecnica da parte del Responsabile del
        trattamento.
    </li>
</ol>
<p>
	I trattamenti connessi ai servizi di questo sito sono curati solo da
	personale designato quale responsabile o autorizzato al trattamento dal
	Titolare del trattamento e da Studio Amica Srl
	<br/>
	La presente informativa si riferisce, quindi, alle operazioni di
	trattamento effettuate su dati raccolti in modo automatico (dati di
	navigazione) nel sito o a seguito di iscrizione al servizio erogato e
	utilizzo della piattaforma telematica ovvero a seguito di invio modulo di
	contatto dell'assistenza tecnica.
</p>

<h3>Base giuridica del trattamento </h3>
<p>
	I dati personali indicati sono trattati dal Titolare del trattamento per la
	gestione dei rapporti contrattuali e pre-contrattuali con gli operatori
	economici, nell'esecuzione dei propri compiti di interesse pubblico o
	comunque connessi all'esercizio dei propri pubblici poteri o per adempiere
	a obblighi di legge. Nel caso di trattamento di categorie particolari di
	dati o di dati relativi a condanne penali o reati la base giuridica ricade
	nei motivi di rilevante interesse pubblico perseguito dalla Stazione
    Appaltante.
</p>
<h3>
    Trasferimenti in paesi extra UE e luogo di trattamento dei dati
</h3>
<p>
	I dati personali sono conservati su server ubicati all'interno dell'Unione
	Europea. Resta in ogni caso inteso che il Titolare, ove si rendesse
	necessario, avr&#224; facolt&#224; di spostare i server anche extra-UE. In
	tal caso, il Titolare assicura sin d'ora che il trasferimento dei dati
	extra-UE avverr&#224; in conformit&#224; alle disposizioni di legge
	applicabili, previa stipula delle clausole contrattuali standard previste
	dalla Commissione Europea.
</p>
<p>
	Allo stato attuale, i trattamenti connessi ai servizi web di questo sito
	hanno luogo presso la sede del Titolare (e sui sistemi in cloud del
	responsabile Studio Amica Srl) e sono curati solo da personale tecnico
	degli uffici incaricati del trattamento o da responsabili esterni
	opportunamente nominati. Nessun dato derivante dal servizio web verr&#224;
	diffuso.
</p>
<p>
	Studio Amica Srl si avvale di data center, situati in Europa.
</p>
<h3>Tipi di dati trattati</h3>
<p>
	<strong>Dati di navigazione</strong>
</p>
<p>
	I sistemi informatici e le procedure software preposte al funzionamento di
	questa piattaforma telematica acquisiscono, nel corso del loro normale
	esercizio, alcuni dati personali la cui trasmissione &#232; implicita
	nell'uso dei protocolli di comunicazione di Internet; si tratta di
	informazioni che non sono raccolte per essere associate a interessati
	identificati, ma che per loro stessa natura potrebbero, attraverso
	elaborazioni ed associazioni con dati detenuti da terzi, permettere di
	identificare gli utenti (in questa categoria di dati rientrano gli
	indirizzi IP o i nomi a dominio dei computer utilizzati dagli utenti che si
	connettono al sito, gli indirizzi in notazione URI - Uniform Resource
	Identifier - delle risorse richieste, l'orario della richiesta, il metodo
	utilizzato nel sottoporre la richiesta al server, la dimensione del file
	ottenuto in risposta, il codice numerico indicante lo stato della risposta
	data dal server ed altri parametri relativi al sistema operativo e
	all'ambiente informatico dell'utente).
	<br/>
	Questi dati vengono utilizzati al solo fine di ricavare informazioni
	statistiche anonime sull'uso del sito e per controllarne il corretto
	funzionamento e vengono cancellati immediatamente dopo l'elaborazione. I
	dati potrebbero essere utilizzati per l'accertamento di responsabilit&#224;
	in caso di ipotetici reati informatici ai danni del sito. In ogni caso i
	dati sui contatti web non persistono per pi&#249; di sette giorni.
	<br/>
	Si precisa che i dati di navigazione potrebbero essere usati per
	l'accertamento di responsabilit&#224; in caso di eventuali reati
	informatici ai danni della piattaforma, conformemente alle procedure
	vigenti presso le Autorit&#224; competenti.
</p>
<p>
	<br/>
	<strong>Dati forniti volontariamente dall'utente</strong>
</p>
<p>
	L'inserimento esplicito e volontario di dati personali su questa
	piattaforma comporta la successiva acquisizione degli stessi al fine di
	elaborare l'istanza di partecipazione alla procedura.
</p>
<p>
	I dati contrassegnati come obbligatori sono necessari per completare la
	procedura (registrazione alla piattaforma, caricamento delle informazioni
	richieste al fine di partecipare alla procedura). In caso di mancato
	conferimento, non sar&#224; possibile compiere le operazioni necessarie a
	dare seguito alla richiesta.
</p>
<p>
	I dati oggetto di trattamento sono dati comuni (anagrafici, ragione
	sociale, dati di contatto, storia professionale, dati di tipo
	economico-finanziario, patrimoniale e contabile, nonch&#233; dati
	giudiziari (dati che possono rivelare l'esistenza di determinati
	provvedimenti giudiziari soggetti ad iscrizione nel casellario giudiziale,
	come i provvedimenti penali di condanna definitivi, la liberazione
	condizionale, il divieto od obbligo di soggiorno, le misure alternative
	alla detenzione; ovvero la qualit&#224; di imputato o di indagato).
</p>
